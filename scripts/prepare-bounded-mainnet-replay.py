def parse_checkpoint_info(path: Path) -> dict[str, str]:
    info_path = path / "CHECKPOINT_INFO"
    if not info_path.exists():
        return {}
    info: dict[str, str] = {}
    for line in info_path.read_text(encoding="utf-8").splitlines():
        if not line.strip() or line.strip().startswith("#") or "=" not in line:
            continue
        key, value = line.split("=", 1)
        info[key.strip()] = value.strip()
    return info


def resolve_state_checkpoint(checkpoint: Path, info: dict[str, str]) -> Path | None:
    candidates: list[Path] = []
    if info.get("mpt_dir"):
        mpt_path = Path(info["mpt_dir"])
        candidates.append(mpt_path if mpt_path.is_absolute() else checkpoint / mpt_path)
        candidates.append(mpt_path)
    candidates.extend([checkpoint / "mpt", checkpoint / "state", checkpoint / "Data_MPT"])
    for candidate in candidates:
        if candidate.is_dir() and (candidate / "CURRENT").exists():
            return candidate
    return None


def toml_quote(value: Path | str) -> str:
    text = str(value)
    return '"' + text.replace("\\", "\\\\").replace('"', '\\"') + '"'


