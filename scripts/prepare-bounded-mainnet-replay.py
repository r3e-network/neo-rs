def step_by_name(plan: dict, name: str) -> dict:
    for step in plan.get("steps", []):
        if step.get("name") == name:
            return step
    raise KeyError(f"plan is missing step: {name}")


def build_replay_plan(
    *,
    checkpoint: Path,
    work_root: Path,
    label: str,
    start_height: int | None = None,
    target_height: int,
    addresses: list[str],
    node_bin: Path = Path(DEFAULT_NODE_BIN),
    probe_bin: Path = Path(DEFAULT_PROBE_BIN),
    reference_rpc: str = DEFAULT_REFERENCE_RPC,
    rpc_port: int = DEFAULT_RPC_PORT,
    p2p_port: int = DEFAULT_P2P_PORT,
    metrics_port: int = DEFAULT_METRICS_PORT,
) -> dict:
    checkpoint = checkpoint.resolve()
    work_dir = (work_root / label).resolve()
    chain_source = checkpoint / "data"
    info = parse_checkpoint_info(checkpoint)
    if start_height is None and info.get("height"):
        start_height = int(info["height"])
    state_source = resolve_state_checkpoint(checkpoint, info)
    state_present = state_source is not None
    validator_enabled = state_present and start_height is not None
    chain_db = work_dir / "data"
    state_db = work_dir / "mpt"
    config_path = work_dir / "neo-bounded-replay.toml"
    log_file = work_dir / "neo-node.log"
    status_file = work_dir / "stateroot-validation.json"
    resume_file = work_dir / "stateroot-last-validated"
    mode = "full-state" if state_present else "storage-sample"

    address_args = []
    for address in addresses:
        address_args.extend(["--address", address])

    validator_command = [
        "python3",
        "scripts/continuous-stateroot-validation.py",
        "--local-config",
        str(config_path),
        "--stop-at",
        str(target_height),
        "--status-file",
        str(status_file),
        "--resume-file",
        str(resume_file),
    ]
    if start_height is not None:
        validator_command[4:4] = ["--start", str(start_height)]

    replay_with_repairs_command = [
        "python3",
        "scripts/run-bounded-replay-with-repairs.py",
        "--config",
        str(config_path),
        "--db",
        str(chain_db),
        "--log",
        str(log_file),
        "--target-height",
        str(target_height),
        "--node-bin",
        str(node_bin),
        "--probe-bin",
        str(probe_bin),
        "--rpc",
        f"http://127.0.0.1:{rpc_port}",
        "--reference-rpc",
        reference_rpc,
    ]

    steps: list[dict[str, Any]] = [
        {
            "name": "copy-chain-checkpoint",
            "source": str(chain_source),
            "destination": str(chain_db),
            "required": True,
        },
        {
            "name": "copy-state-checkpoint",
            "source": str(state_source) if state_source else None,
            "destination": str(state_db),
            "required": state_present,
            "enabled": state_present,
        },
        {
            "name": "write-config",
            "path": str(config_path),
        },
        {
            "name": "node-preflight",
            "command": [str(node_bin), "--config", str(config_path), "--check-all"],
        },
        {
            "name": "run-node",
            "command": [str(node_bin), "--config", str(config_path)],
            "log": str(log_file),
        },
        {
            "name": "run-node-with-repairs",
            "command": replay_with_repairs_command,
            "log": str(log_file),
        },
        {
            "name": "state-root-validator",
            "enabled": validator_enabled,
            "command": validator_command,
            "reason": state_root_validator_disabled_reason(
                state_present=state_present,
                start_height=start_height,
            ),
        },
        {
            "name": "offline-gas-compare",
            "command": [
                "python3",
                "scripts/compare-offline-gas-storage.py",
                "--db",
                str(chain_db),
                "--probe-bin",
                str(probe_bin),
                "--reference-rpc",
                reference_rpc,
                *address_args,
            ],
        },
    ]

    return {
        "mode": mode,
        "checkpoint": {
            "path": str(checkpoint),
            "label": info.get("label"),
            "info": info,
        },
        "chain_checkpoint": {
            "path": str(chain_source),
            "present": chain_source.is_dir() and (chain_source / "CURRENT").exists(),
        },
        "state_checkpoint": {
            "path": str(state_source) if state_source else None,
            "present": state_present,
        },
        "work": {
            "root": str(work_root),
            "dir": str(work_dir),
            "chain_db": str(chain_db),
            "state_db": str(state_db),
            "config_path": str(config_path),
            "log_file": str(log_file),
            "status_file": str(status_file),
            "resume_file": str(resume_file),
        },
        "target_height": target_height,
        "start_height": start_height,
        "rpc_port": rpc_port,
        "p2p_port": p2p_port,
        "metrics_port": metrics_port,
        "steps": steps,
        "config_text": render_config(
            chain_db=chain_db,
            state_db=state_db,
            state_enabled=state_present,
            rpc_port=rpc_port,
            p2p_port=p2p_port,
            metrics_port=metrics_port,
            log_file=log_file,
        ),
    }


def copy_checkpoint_dir(source: Path, destination: Path) -> None:
    if not source.is_dir():
        raise FileNotFoundError(f"checkpoint source is missing: {source}")
    shutil.copytree(source, destination)


def state_root_validator_disabled_reason(
    *,
    state_present: bool,
    start_height: int | None,
) -> str | None:
    if not state_present:
        return "Checkpoint has no StateRoot/MPT directory; use storage-sample mode until a full-state checkpoint is available."
    if start_height is None:
        return "Checkpoint start height is unknown; pass --start-height so state-root validation starts at the checkpoint height."
    return None


def prepare_workspace(plan: dict, *, force: bool = False) -> dict:
    work_dir = Path(plan["work"]["dir"])
    if work_dir.exists():
        if not force:
            raise FileExistsError(f"work directory already exists: {work_dir}")
        shutil.rmtree(work_dir)
    work_dir.mkdir(parents=True)

    copy_checkpoint_dir(
        Path(step_by_name(plan, "copy-chain-checkpoint")["source"]),
        Path(plan["work"]["chain_db"]),
    )
    state_step = step_by_name(plan, "copy-state-checkpoint")
    if state_step.get("enabled"):
        copy_checkpoint_dir(Path(state_step["source"]), Path(plan["work"]["state_db"]))

    config_path = Path(plan["work"]["config_path"])
    config_path.write_text(plan["config_text"], encoding="utf-8")
    return {
        "prepared": True,
        "mode": plan["mode"],
        "work_dir": str(work_dir),
        "config_path": str(config_path),
    }


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Prepare a bounded MainNet replay workspace from a validated checkpoint."
    )
    parser.add_argument("--checkpoint", required=True, type=Path)
    parser.add_argument("--work-root", default=Path(DEFAULT_WORK_ROOT), type=Path)
    parser.add_argument("--label", required=True)
    parser.add_argument("--start-height", default=None, type=int)
    parser.add_argument("--target-height", required=True, type=int)
    parser.add_argument("--address", action="append", default=[])
    parser.add_argument("--node-bin", default=Path(DEFAULT_NODE_BIN), type=Path)
    parser.add_argument("--probe-bin", default=Path(DEFAULT_PROBE_BIN), type=Path)
    parser.add_argument("--reference-rpc", default=DEFAULT_REFERENCE_RPC)
    parser.add_argument("--rpc-port", default=DEFAULT_RPC_PORT, type=int)
    parser.add_argument("--p2p-port", default=DEFAULT_P2P_PORT, type=int)
    parser.add_argument("--metrics-port", default=DEFAULT_METRICS_PORT, type=int)
    parser.add_argument("--prepare", action="store_true")
    parser.add_argument("--force", action="store_true")
    return parser.parse_args()


def main() -> int:
    args = parse_args()
    plan = build_replay_plan(
        checkpoint=args.checkpoint,
        work_root=args.work_root,
        label=args.label,
        start_height=args.start_height,
        target_height=args.target_height,
        addresses=args.address,
        node_bin=args.node_bin,
        probe_bin=args.probe_bin,
        reference_rpc=args.reference_rpc,
        rpc_port=args.rpc_port,
        p2p_port=args.p2p_port,
        metrics_port=args.metrics_port,
    )
    if args.prepare:
        plan["prepare_result"] = prepare_workspace(plan, force=args.force)
    print(json.dumps(plan, indent=2, sort_keys=True))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
